#include <stdlib.h>
#include <stdio.h>

int main()
{

  int num1 = 8;
  int num2 = 5;
  float num3 = 8;
  float num4 = 5;

  // Suma +
  int sum = num1 + num2;
  printf("%i \n", sum);

  // Resta -
  int res = num1 - num2;
  printf("%i \n", res);

  // Multiplicacion *
  int x = num1 * num2;
  printf("%i \n", x);

  // Division /
  float div = num3 / num4;
  printf("%f \n",div);

  // Modulo %
  int module = num1 % num2;
  printf("%f \n", module);

}